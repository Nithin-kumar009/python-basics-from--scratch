print(type(10))            # int
print(type(10.5))          # float
print(type(3 + 4j))        # complex
print(type("Hello"))       # str
print(type([1, 2, 3]))     # list
print(type((1, 2, 3)))     # tuple
print(type({1, 2, 3}))     # set
print(type({"a": 1}))      # dict
print(type(True))          # bool
print(type(None))          # NoneType